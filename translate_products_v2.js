function translateProductName(arabicName) {
    let translated = arabicName;

    // 0. Clean formatting chars
    translated = translated.replace(/\*/g, ' x ');
    translated = translated.replace(/-/g, ' - ');

    // 1. Replace Brands and Terms
    Object.keys(translations).forEach(key => {
        try {
            // Escape special chars like '(', ')' if needed
            const safeKey = key.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
            const regex = new RegExp(safeKey, 'g');
            translated = translated.replace(regex, translations[key]);
        } catch (e) {
            translated = translated.split(key).join(translations[key]);
        }
    });

    // 2. Cleanup Residuals
    // Remove "ال" (Al) if attached to start of words (e.g., AlGül -> Gül)
    translated = translated.replace(/\bال/g, '');

    // Remove "لل" (Li)
    translated = translated.replace(/\bلل/g, '');

    // 3. Fix numbers/units spacing and artifacts
    translated = translated.replace(/(\d+)(kg|gr|ml)/gi, '$1 $2');

    // Specific fix for "mlون" -> "Renkli" (if mixed)
    translated = translated.replace(/mlون/g, 'Renkli');

    // Fix "Çuvalات" -> "Çuval"
    translated = translated.replace(/Çuvalات/g, 'Çuval');

    // Remove any remaining Arabic characters range [\u0600-\u06FF]
    // This is the final safety net to ensure "Turkish Only" request is met.
    translated = translated.replace(/[\u0600-\u06FF]/g, '');

    // 4. Final Cleanup
    // Remove non-alphanumeric trailing garbage or multiple spaces
    translated = translated.replace(/\s+/g, ' ').trim();
    // Remove trailing non-word chars like - or /
    translated = translated.replace(/[-/]+$/, '');

    return translated.trim();
}

